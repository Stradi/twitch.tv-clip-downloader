TEXT_RESET = "\u001b[0m"

COLOR_BLACK = "\u001b[30m"
COLOR_RED = "\u001b[31m"
COLOR_GREEN = "\u001b[32m"
COLOR_YELLOW = "\u001b[33m"
COLOR_BLUE = "\u001b[34m"
COLOR_MAGENTA = "\u001b[35m"
COLOR_CYAN = "\u001b[36m"
COLOR_WHITE = "\u001b[37m"
COLOR_BRIGHTBLACK = "\u001b[30m;1m"
COLOR_BRIGHTRED = "\u001b[31m;1m"
COLOR_BRIGHTGREEN = "\u001b[32m;1m"
COLOR_BRIGHTYELLOW = "\u001b[33m;1m"
COLOR_BRIGHTBLUE = "\u001b[34m;1m"
COLOR_BRIGHTMAGENTA = "\u001b[35m;1m"
COLOR_BRIGHTCYAN = "\u001b[36m;1m"
COLOR_BRIGHTWHITE = "\u001b[37m;1m"

BG_COLOR_BLACK = "\u001b[40m"
BG_COLOR_RED = "\u001b[41m"
BG_COLOR_GREEN = "\u001b[42m"
BG_COLOR_YELLOW = "\u001b[43m"
BG_COLOR_BLUE = "\u001b[44m"
BG_COLOR_MAGENTA = "\u001b[45m"
BG_COLOR_CYAN = "\u001b[46m"
BG_COLOR_WHITE = "\u001b[47m"
BG_COLOR_BRIGHTBLACK = "\u001b[40m;1m"
BG_COLOR_BRIGHTRED = "\u001b[41m;1m"
BG_COLOR_BRIGHTGREEN = "\u001b[42m;1m"
BG_COLOR_BRIGHTYELLOW = "\u001b[43m;1m"
BG_COLOR_BRIGHTBLUE = "\u001b[44m;1m"
BG_COLOR_BRIGHTMAGENTA = "\u001b[45m;1m"
BG_COLOR_BRIGHTCYAN = "\u001b[46m;1m"
BG_COLOR_BRIGHTWHITE = "\u001b[47m;1m"

TEXT_BOLD = "\u001b[1m"
TEXT_UNDR = "\u001b[4m"
TEXT_RVRS = "\u001b[7m"

def colorize(text, color=COLOR_WHITE, bg=COLOR_BLACK):
  return bg + color + text + TEXT_RESET

def bold(text):
  return TEXT_BOLD + text + TEXT_RESET

def undr(text):
  return TEXT_UNDR + text + TEXT_RESET

def rvrs(text):
  return TEXT_RVRS + text + TEXT_RESET